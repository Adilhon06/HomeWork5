public class Hero {
    private int health;
    private int damage;
    private String superpower;

    Hero(int health, int damage, String superpower){
    }
    Hero(int health, int damage){
    }
    public int getHealth() {
        return health;
    }

    public int getDamage() {
        return damage;
    }

    public String getSuperpower() {
        return superpower;
    }
}
